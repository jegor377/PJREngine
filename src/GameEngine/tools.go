	colors[0x3] = sdl.Color{0x00, 0xBF, 0xA5, 0xFF} // #00BFA5 AQUA
	colors[0x4] = sdl.Color{0xB7, 0x1C, 0x1C, 0xFF} // #B71C1C RED
